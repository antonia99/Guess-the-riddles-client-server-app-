
public class Ghicitoare {
 public String ghicitoare;
 public String raspuns;
 
public Ghicitoare(String ghicitoare, String raspuns) {
	super();
	this.ghicitoare = ghicitoare;
	this.raspuns = raspuns;
}
public String getGhicitoare() {
	return ghicitoare;
}
public void setGhicitoare(String ghicitoare) {
	this.ghicitoare = ghicitoare;
}
public String getRaspuns() {
	return raspuns;
}
public void setRaspuns(String raspuns) {
	this.raspuns = raspuns;
}
 
}